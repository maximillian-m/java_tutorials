package com.maximillian.java_encapsulation;

public class Person {
    private int id = 1;
    private String name = "Jane";

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
